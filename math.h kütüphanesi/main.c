#include <stdio.h>
#include <stdlib.h>
#include <math.h>


int main(void)
{
    printf("%.2f\n",ceil(4.3)); //kendinden büyük en yakın tam sayıya yuvarlama//
    
    printf("%.2f\n",floor(4.9)); //kendinden küçük en yakın tam sayıya yuvarlama//
    
    printf("%.2f\n",sqrt(25)); //karekök içine alma//
    
    printf("%.2f\n",pow(3,3)); //üslü şekilde yazma//
    
    printf("%d\n",abs(-15)); //mutlak değer şeklinde yazma,int değeri tam sayı olmak zorunda float,double olamaz//
    
    
    return (0);
    
}



